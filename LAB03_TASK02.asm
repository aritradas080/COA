.MODEL SMALL
.STACK 100h
.DATA

MSG1 DB 0AH,0DH,'HELLO WORLD$'  
MSG2 DB ' $'
MSG3 DB 'BYE WORLD$'

.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV CX,5

PRINT_LOOP:
  
LEA DX, MSG1
MOV AH, 9
INT 21h
DEC CX

JG PRINT_LOOP 

MOV AH,2
MOV DX,0AH
INT 21H
MOV AH,2
MOV DX,0DH
INT 21H

LEA DX,MSG2
MOV AH,9
INT 21H

MOV AH,2
MOV DX,0AH
INT 21H
MOV AH,2
MOV DX,0DH
INT 21H

LEA DX,MSG3
MOV AH,9
INT 21H

Exit:
MOV AH,4CH
INT 21h


MAIN ENDP
END MAIN