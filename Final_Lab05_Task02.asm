.MODEL SMALL
.STACK 100H
.DATA       

MSG1 DB 0AH,0DH,'INPUT : $'
MSG2 DB 0AH,0DH,'THE ASCII CODE OF $'
MSG3 DB ' IN HEX IS : $' 

.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX  

INPUT:
LEA DX,MSG1
MOV AH,9
INT 21H

;TAKING INPUT
MOV AH,1
INT 21H
MOV BL,AL 

CMP BL,0DH
JE END   

LEA DX,MSG2
MOV AH,9
INT 21H  


MOV AH,2  
MOV DL,BL 
INT 21H 

LEA DX,MSG3
MOV AH,9
INT 21H     

MOV CX,4

CONVERT:
MOV DL,BH

;SHIFT LEFT 4 TIMES
SHR DL,4
CMP DL,9
JLE NUM
ADD DL,55D
JMP DISPLAY

NUM:
ADD DL,30H

DISPLAY:
MOV AH,2  
;ROTATE CARRY LEFT 4 TIMES
RCL BX,4
INT 21H 
LOOP CONVERT
JMP INPUT

END:
MOV AH,4CH
INT 21H    

MAIN ENDP
END MAIN