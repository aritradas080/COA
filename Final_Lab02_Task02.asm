.MODEL SMALL
.STACK 100h
.DATA

  

.CODE

MAIN PROC

       
MOV AH, 1
INT 21H 

CMP AL, 'A' 
JE CR_RETURN

CMP AL, 'B'
JE LINE_FEED


MOV AH, 4CH
INT 21H
JMP END


CR_RETURN:
MOV AH, 2
MOV DL, 0DH
INT 21H
JMP END

LINE_FEED:
MOV AH, 2
MOV DL, 0AH
INT 21H


END:
MOV AH,4CH
INT 21h


MAIN ENDP
END MAIN


